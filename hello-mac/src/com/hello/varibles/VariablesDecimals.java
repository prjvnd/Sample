package com.hello.varibles;

public class VariablesDecimals {
   // Float and double 
	//Float is single precision 
	//Double is double precision (more precise than float)
	
	
	public static void main(String[] args) {
		int myIntVal = 5/3;
		float myfloatVal = 5f/3f;
		double mydoubleVal = 5d/3d;
		// adding d will make it precise 
		System.out.println("Int value "+myIntVal);

		System.out.println("Float value "+myfloatVal);
		System.out.println("Double value "+mydoubleVal);
	}
	
	
}
